// Find all the odd numbers from 61 to 100

let num1 = 61;

while(num1 <=  100) {
    console.log(num1);
    
    if(num1 % 2 !== 0){
        console.log('Odd:', num1);
    }
    num1++;
}


// Find all the even numbers from 78 to 98.

let num2 = 78;

while(num2 <=  98) {
    console.log(num2);
    
    if(num2 % 2 === 0){
        console.log('Even:', num2);
    }
    num2++;
}