let num = 1; // loop variable
while (num < 5) { // loop condition
    console.log(num); // loop statement
    num++; // loop variable change
}

// syntax for-loop --> for(initialization; condition; increment/decrement)

for (let num = 1; num < 5; num++) {
    console.log(num);
}

// even
for (let num = 0; num < 20; num=num+2) {
    console.log(num);
}

// odd
for (let num = 1; num < 20; num=num+2) {
    console.log(num);
}